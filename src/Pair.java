public class Pair {
    /* Attributes */
    
    /* the first element of the pair */
    public double f; 

    /* the second element of the pair */
    public double s; 

    /**
    Creates a basic pair
    @param f the first element of the pair
    @param s the second element of the pair
    */
    public Pair(double f, double s) {
        this.f = f;
        this.s = s; 
    }

}
